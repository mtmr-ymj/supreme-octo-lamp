package process1_23;

public class process {
	// プライベートの変数を設定する
	private String name;
	private double body;
	private int speed;
	
	// 名前のgetter
	public String getName(){
		return this.name;
	}
	// 名前のsetter
	public void setName(String name) {
		this.name = name;
	}
	
	// 体長のgetter
	public double getBody() {
		return this.body;
	}
	// 体長のsetter
	public void setBody(double body) {
		this.body = body;
	}
	
	// 速度のgetter
	public int getSpeed() {
		return this.speed;
	}
	// 速度のsetter
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	

}
